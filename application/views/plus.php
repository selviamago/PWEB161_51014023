<p>Cara untuk membuat puding jagung</p>
<p>Bahan Puding Jagung</p>

200 gram jagung manis pipil<br>
100 ml air<br>
400 ml susu cair<br>
1/2 bks agar-agar bubuk<br>
100 gram gula pasir<br>
1/4 sendok teh vanili bubuk<br>
1/4 sendok teh garam<br>
1 sendok teh tepung maizena<br>
5 sendok makan susu kental manis putih<br>

Cara Membuat Puding Jagung<br>

1. Blender jagung manis dan 100 ml air, sisihkan.<br>
2. Campur agar-agar bubuk, gula pasir, vanili bubuk, garam, dan tepung maizena. Aduk rata.<br>
3. Masukkan susu cair dan jagung manis yang telah dihaluskan, aduk rata.<br>
4. Masak di atas api kecil hingga mendidih.<br>
5. Matikan api, tambahkan susu kental manis putih, aduk rata.<br>
6. Tuang agar-agar ke dalam cetakan yang telah dibasahi, biarkan beku.<br>
7. Keluarkan dari cetakan, sajikan.<br>